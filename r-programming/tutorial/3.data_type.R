## Data Type

## 1. numeric
age <- 32
print(32)
class(age)

## 2. character
my_name <- "Gun"
my_university <- "Chulalongkorn University"
print(my_name)
print(my_university)
class(my_name); class(my_name)

## 3. logical
result <- (1 + 1 == 2)
print(result)
class(result)

## 4. factor
animals <- c("Dog", "Cat", "Cat", "Hippo")
class(animals)
print(animals)

animals <- factor(animals)
class(animals)
print(animals)

## 5. date/time
time_now <- Sys.time()
class(time_now)












